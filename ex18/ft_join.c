#include <stdio.h>
char *ft_join(char **tab, char *sep)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (*tab[i] != '\0')
	{
		i++;
		while (sep[j] != '\0')
		{
			*tab[i] = sep[j];
			i++;
			j++;
		}
		*tab[i] = '\0';
	}
	return *(tab);
}
