int ft_unmatch(int *tab, int length)
{
	tab[length];

	int i;
	int j;

	i = 0;
	j = 1;
	while (tab[i] != '\0')
	{
		while (tab[j] != '\0')
		{
			if (tab[i] == tab[j])
			{
				tab[i] = '\0';
				tab[j] = tab[i];
			}
			j++;
		}
		i++;
	}
}


